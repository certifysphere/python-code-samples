# Lists
fruits = ["apple", "banana", "orange"]
print(fruits[0])  # Output: "apple"

# Tuples
point = (2, 3)
print(point[1])  # Output: 3

# Dictionaries
person = {"name": "John", "age": 25, "city": "New York"}
print(person["age"])  # Output: 25

